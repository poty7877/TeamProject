package hairshop.dto;

public class DesignerDTO {

	private String dno;
	private String dname;
	private String dsno;
	private String dopen;
	private String dclose;
	private String dofile;
	private String dsfile;
	
	public DesignerDTO() {}

	public String getDno() {
		return dno;
	}

	public String getDname() {
		return dname;
	}

	public String getDsno() {
		return dsno;
	}

	public String getDopen() {
		return dopen;
	}

	public String getDclose() {
		return dclose;
	}

	public String getDofile() {
		return dofile;
	}

	public String getDsfile() {
		return dsfile;
	}

	public void setDno(String dno) {
		this.dno = dno;
	}

	public void setDname(String dname) {
		this.dname = dname;
	}

	public void setDsno(String dsno) {
		this.dsno = dsno;
	}

	public void setDopen(String dopen) {
		this.dopen = dopen;
	}

	public void setDclose(String dclose) {
		this.dclose = dclose;
	}

	public void setDofile(String dofile) {
		this.dofile = dofile;
	}

	public void setDsfile(String dsfile) {
		this.dsfile = dsfile;
	}
	
	
	
	
}//class end
